#include "philo.h"

uint64_t	get_time(void)
{
	struct timeval	time;

	gettimeofday(&time, NULL);
	return ((time.tv_sec * 1000) + (time.tv_usec / 1000));
}

void	ft_msleep(uint64_t time)
{
	uint64_t		start;

	start = get_time();
	while ((get_time() - start) < time)
		usleep(100);
}